                <h3>Jugabilidad</h3>
                <p class="text-white">The Witcher 3: Wild Hunt es un juego de rol de acción con una perspectiva en
                    tercera persona. Los jugadores controlan a Geralt de Rivia, un cazador de monstruos conocido como
                    brujo. Geralt camina, corre, rueda y esquiva, y (por primera vez en la serie) salta, trepa y nada.
                    Tiene una variedad de armas, incluidas bombas, una ballesta y dos espadas (una de acero y otra de
                    plata). La espada de acero se usa principalmente para matar humanos mientras que la espada de plata
                    es más efectiva contra criaturas y monstruos. Los jugadores pueden sacar, cambiar y envainar sus
                    espadas a voluntad. Hay dos modos de ataque cuerpo a cuerpo; los ataques ligeros son rápidos pero
                    débiles, y los ataques pesados son lentos y fuertes. Los jugadores pueden bloquear y contrarrestar
                    los ataques enemigos con sus espadas. Las espadas tienen resistencia limitada y requieren reparación
                    regular. Además de los ataques físicos, Geralt tiene cinco signos mágicos a su disposición: Aard,
                    Axii, Igni, Yrden y Quen. Aard hace que Geralt desate una explosión telequinética, Axii confunde a
                    los enemigos y los ralentiza, Igni los quema, Yrden los ralentiza (además de actuar como trampa para
                    apariciones) y Quen ofrece a los jugadores un escudo protector temporal. Los signos usan resistencia
                    y no pueden usarse indefinidamente. Los jugadores pueden usar mutágenos para aumentar el poder
                    mágico de Geralt. Pierdes salud cuando eres atacado por enemigos, aunque usar armadura puede ayudar
                    a reducir la pérdida de salud. La salud se restaura con la meditación o los consumibles, como
                    alimentos y pociones. Los jugadores ocasionalmente controlan a Ciri, la hija adoptiva de Geralt, que
                    puede teletransportarse distancias cortas. El juego tiene inteligencia artificial avanzada (IA)
                    receptiva y entornos dinámicos. El ciclo día-noche influye en algunos monstruos (un hombre lobo se
                    vuelve poderoso durante la noche de luna llena, hay apariciones que solo se muestran al mediodía,
                    etc.). Los jugadores pueden aprender sobre sus enemigos y prepararse para el combate leyendo el
                    bestiario del juego. Cuando matan a un enemigo, pueden saquear su cadáver en busca de objetos de
                    valor. El sentido de brujo de Geralt permite a los jugadores encontrar objetos de interés, incluidos
                    elementos que se pueden recoger y vender. Los artículos se almacenan en el inventario, que se puede
                    ampliar comprando actualizaciones (mediante las alforjas o una habilidad pasiva). Los jugadores
                    pueden vender objetos a vendedores o utilizarlos para crear pociones y bombas. Pueden visitar a los
                    herreros para crear nuevas armas y armaduras con lo que han reunido. Existen dos tipos de artesanos
                    que fabrican armaduras o armas (si fabrican armas no pueden fabricar armaduras y viceversa). El
                    precio de un artículo y el costo de su elaboración dependen de la economía local de una región.
                </p>

                <p class="text-white">En el trayecto de la historia, Geralt tendrá que ir recorriendo todo "El
                    Continente" para encontrar a Ciri. El continente está compuesto por zonas distintas, las cuales se
                    pueden seleccionar en el mapa, y viajar de una a otra mediante la opción de "Viaje Rápido" que el
                    juego brinda al jugador. Estas zonas tienen nombre propio y son las siguientes: Huerto Blanco,
                    Velen, Kaer Morhen, Novrigado, Wyzima y las Islas Sekellige (las zonas son desbloqueadas mediante el
                    jugador avanza en el juego). En cuanto a la opción de "Viaje Rápido", se pueden ver en el mapa del
                    juego como postes de color verde que se encuentran en la mayoría de los pueblos alrededor del mapa,
                    e incluso a veces en cualquier camino: comúnmente se pueden hallar en cruces donde se abren dos
                    caminos distintos.
