                <p class="text-white m-0 text-justify">
                <h3>Jugabilidad</h3>
                <p class="text-white">Elden Ring es un videojuego de rol de acción en tercera persona, con una
                    jugabilidad centrada en el combate y la exploración. Presenta características similares de otros
                    juegos desarrollados por FromSoftware, tales como la saga Dark Souls, Bloodborne y Sekiro: Shadows
                    Die Twice. Localizado en un mundo abierto, el jugador puede explorar libremente las Tierras
                    Intermedias y sus seis áreas principales. Estas localizaciones varían desde Necrolimbo, una zona de
                    prados verdes y ruinas antiguas, hasta Caelid, un páramo poblado de monstruos no-muertos. Las
                    zonas de mundo abierto se pueden explorar con la montura espectral del personaje jugador,
                    Torrentera, como principal método de viaje, además de la opción de viaje rápido fuera de combate. A
                    lo largo del juego, el jugador puede encontrarse personajes no jugador (NPC) y enemigos, incluyendo
                    semidioses que gobiernan cada área principal y sirven como los principales jefes del juego.
                    Además de zonas de mundo abierto, el juego incluye también mazmorras escondidas, como catacumbas,
                    túneles y cuevas donde los jugadores pueden pelear contra jefes y conseguir objetos valiosos.
                </p>

                <p class="text-white">El jugador elige una clase de personaje al inicio del juego, lo cual determina los
                    hechizos y equipamiento con el que comienza la partida. El combate contra los enemigos puede ser a
                    melé o a distancia usando armas a distancia o hechizos. Se puede bloquear los ataques de los
                    enemigos mediante escudos o esquivando. Los hechizos permiten al jugador fortalecer sus armas,
                    combatir a los enemigos a distancia o recuperar puntos de salud. En Elden Ring las armas se pueden
                    mejorar utilizando las Cenizas de Guerra, objetos que añaden nuevas habilidades a las armas. Las
                    Cenizas de Guerra se pueden aplicar o retirar en las armas, y cada Ceniza añade un Arte de Combate,
                    una habilidad especial que puede ser utilizada en combate. Además del combate directo, las
                    mecánicas de sigilo se pueden utilizar para evitar a los enemigos o acercarse a estos para realizar
                    un ataque crítico escondido.
                </p>

                <p class="text-white">Los Sitios de Gracia, puntos de control, están dispersos a lo largo del mapa del
                    juego y actúan como lugares donde el jugador puede aumentar sus estadísticas, cambiar los hechizos
                    memorizados, intercambiar entre Cenizas de Guerra o viajar a otros Sitios de Gracia mediante la
                    mecánica de viaje rápido. Al morir, el jugador reaparece en el último Sitio de Gracia donde haya
                    descansado; también puede elegir si reaparecer en una Estaca de Márika si muere en su zona de
                    influencia. Para incrementar sus estadísticas en los Sitios de Gracia, el jugador tiene
                    que gastar runas, una moneda dentro del juego adquirida al matar a enemigos. Las runas también
                    se pueden usar para comprar objetos y mejorar armas y equipamiento. Morir en Elden Ring hace que el
                    jugador pierda todas las runas coleccionadas en el lugar de muerte y si el jugador muere otra vez
                    antes de recuperar las runas, se pierden para siempre.
                </p>

                <p class="text-white">Elden Ring incluye mecánicas de fabricación, para las que hace falta materiales
                    para crear objetos. Para fabricar un objeto concreto, el jugador debe tener una receta de
                    fabricación. Las recetas se pueden encontrar dentro de objetos llamados Libros de Preparación, los
                    cuales están dispersos por el mundo. Los materiales se pueden obtener al derrotar a enemigos,
                    explorar el mundo o comerciando con NPCs mercaderes. Los objetos fabricables varían desde dardos
                    envenenados, tarros explosivos u objetos consumibles que aumentan temporalmente la fuerza del
                    jugador en combate. De manera similar a los juegos de la saga Dark Souls, el jugador puede
                    invocar a NPCs amistosos, llamados espíritus, para combatir a los enemigos. Cada tipo de espíritu
                    requiere su equivalente en Cenizas de Espíritu para ser invocado; se pueden encontrar diferentes
                    tipos de Cenizas de Espíritu a lo largo del mundo. También se les puede invocar cerca de unas
                    estructuras llamadas Monumentos al Renacimiento, los cuales se encuentran principalmente en zonas
                    amplias y dentro de arenas de combate contra jefes.
                </p>

                <p class="text-white">Elden Ring tiene modo multijugador que permite a los jugadores ser invocados para
                    jugar en cooperativo o en un combate jugador contra jugador (PvP) a través de Internet. Para
                    jugar en cooperativo hay que colocar una Señal de invocación en el suelo que aparece en el mundo de
                    los jugadores que hayan utilizado un Remedio de Dedo Doblado. Si otro jugador interactúa con la
                    Señal, la persona indicada en esta aparecerá en su mundo. Los jugadores cooperadores permanecen en
                    el mismo mundo hasta que el jefe de la zona haya sido derrotado o hasta que el jugador invocado
                    muera y sea enviado de vuelta a su mundo. El combate PvP requiere utilizar una Señal de invocación
                    para retar a otro jugador en un duelo de manera directa, o usando objetos como un Dedo Sangriento o
                    un Dedo de Recusante para invadir el mundo de otros jugadores. El huésped del mundo también puede
                    utilizar una Lengua del Atormentador para aumentar la probabilidad de ser invadido por otros
                    jugadores, así como reducir el tiempo entre invasiones.
                </p>
