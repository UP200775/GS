                <h3>Jugabilidad</h3>
                <p class="text-white">Breath of the Wild es un videojuego de acción-aventura ambientado en un mundo
                    abierto donde los jugadores pueden explorar el reino de Hyrule mientras controlan a Link. El jugador
                    controla a Link en una perspectiva en tercera persona y en un espacio tridimensional. En cuanto a
                    su estructura, el juego no es lineal, por lo que se le dan pocas instrucciones al jugador y se
                    pueden explorar libremente las áreas del juego, las cuales no tienen entradas o salidas
                    definidas. Breath of the Wild introduce un motor de física lógico y realista, lo que permite a
                    los jugadores abordar los problemas de diferentes maneras en lugar de tratar de encontrar una única
                    solución. El motor de juego define las propiedades físicas de la mayoría de los objetos y
                    controla cómo interactúan con el jugador y entre sí. Su enfoque de diseño da como resultado un
                    mundo generalmente no estructurado e interactivo que recompensa la experimentación y permite la
                    finalización no lineal de la historia.
                </p>

                <p class="text-white">El jugador puede viajar a través del mundo corriendo, escalando, nadando,
                    planeando o a caballo, aunque las acciones están limitadas por la resistencia física de Link.
                    Link puede obtener elementos del medio ambiente, incluidas armas, alimentos y otros recursos. A
                    diferencia de los juegos anteriores de la saga Zelda, las armas en Breath of the WIld se degradan
                    con el uso. Muchos objetos tienen múltiples usos; las armas de madera pueden ser usadas como
                    antorchas y los escudos pueden usarse como tablas de snowboard improvisadas. Los jugadores
                    pueden obtener comida cazando animales, recolectando frutas silvestres o recolectando partes de
                    enemigos derrotados. Al cocinar combinaciones de alimentos o materiales, el jugador puede crear
                    comidas y elixires que pueden reponer la salud y la resistencia de Link, o proporcionar
                    bonificaciones de estado temporales como mayor fuerza o sigilo.
                </p>

                <p class="text-white">Una herramienta importante en el arsenal de Link es la tableta Sheikah, que se
                    puede utilizar para marcar puntos de referencia en un mapa y como cámara en el juego, Link puede
                    actualizarla para añadirle módulos que le otorgan nuevas habilidades como invocar bombas remotas a
                    través de magia, manipular objetos metálicos, detener objetos de manera temporal y formar pilares
                    de hielo en superficies acuosas. En combate, los jugadores pueden fijar objetivos para ataques
                    más precisos, mientras que ciertas combinaciones de botones permiten movimientos ofensivos y
